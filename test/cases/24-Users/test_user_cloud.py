def normalize_errno(err):
    if err is None:
        return None
    if err < 0:
        return (err & 0xFFFFFFFF) & 0xFFFF
    return err & 0xFFFF


def errno_from_exception(e):
    if hasattr(e, "args") and len(e.args) >= 2:
        return e.args[1]
    if hasattr(e, "errno"):
        return e.errno
    return None


